var ASSET_MANAGER = new AssetManager();
var canvas;
var ctx;
var gameEngine;
const SIZE = 16; // For the width and height of the pixle.
const ROW = 50; // This is the size of the grid.
const COL = 50; // This is the size of the grid.
var drawGrid = true; // Set to true if you want grid.
var worldwrapping = false // Turn world wrapping on to destroy the gun.

window.onload = function() {
    canvas = document.getElementById("gameWorld");
    ctx = canvas.getContext("2d");
    gameEngine = new GameEngine();
    var automata = new Automata(gameEngine);
    gameEngine.addEntity(automata);
    gameEngine.board = automata;
    gameEngine.init(ctx);
    gameEngine.start();
}

function Automata() {
    this.grid = GOSPER_50;
};

Automata.prototype = new Entity();
Automata.prototype.constructor = Automata;
Automata.prototype.update = function () {};

/**
 * Paints the pixels
 */
Automata.prototype.draw = function () {
    for (var c = 0; c < COL; c++) {
        for (var r = 0; r < ROW; r++) {
            var cell = this.grid[c][r];
            if (cell === 1) {
                ctx.fillStyle = "#000000";
                ctx.fillRect(r * SIZE, c * SIZE, SIZE, SIZE);
            }
        }
    }
    if (drawGrid) {
        drawLine();
    }
    this.step(this.grid, COL, ROW);
};

/**
 * The method that updates the state of the automata.
 */
Automata.prototype.step = function(grid, col, row) {
    var nextStep = new Array(COL);
    for (var i = 0; i < nextStep.length; i++) {
        nextStep[i] = new Array(ROW);
    }
    for (var c = 0; c < col; c++) {
        for (var r = 0; r < row; r++) {
            if (worldwrapping) {
                var liveNeighbours = 0;
                for (var i = -1; i <= 1; i++) {
                    for (var j = -1; j <= 1; j++) {
                        var x = (c + i + COL) % COL;
                        var y = (r + j + ROW) % ROW;
                        liveNeighbours += grid[x][y];
                    }
                }
                liveNeighbours -= grid[c][r];
                if ((grid[c][r] === 1 && liveNeighbours < 2) || (grid[c][r] === 1 && liveNeighbours > 3)) {
                    nextStep[c][r] = 0;
                } else if (grid[c][r] === 0 && liveNeighbours === 3) {
                    nextStep[c][r] = 1;
                } else {
                    nextStep[c][r] = grid[c][r];
                }
            } else {
                if (r === 0 || r === (row-1) || c === 0 || c === (col-1)) {
                    nextStep[c][r] = grid[c][r];
                } else {
                    var liveNeighbours = 0;
                    for (var i = -1; i <= 1; i++) {
                        for (var j = -1; j <= 1; j++) {
                            var x = (c + i) % COL;
                            var y = (r + j) % ROW;
                            liveNeighbours += grid[c+i][r+j];
                        }
                    }

                    liveNeighbours -= grid[c][r];
                    
                    if ((grid[c][r] === 1 && liveNeighbours < 2) || (grid[c][r] === 1 && liveNeighbours > 3)) {
                        nextStep[c][r] = 0;
                    } else if (grid[c][r] === 0 && liveNeighbours === 3) {
                        nextStep[c][r] = 1;
                    } else {
                        nextStep[c][r] = grid[c][r];
                    }
                }
            }
        }
    }
    this.grid = nextStep;
}

/**
 * Draws the grid on the canvas
 */
function drawLine() {
    for (var r = 0; r < ROW; r++) {
        for (var c = 0; c < COL; c++) {
            ctx.strokeStyle = "#BEBEBE";
            ctx.lineWidth = 1;
            ctx.beginPath();
            ctx.moveTo(SIZE * c, 0);
            ctx.lineTo(SIZE * c, 800);
            ctx.moveTo(0, SIZE * c);
            ctx.lineTo(800, SIZE * c);
            ctx.stroke();
        }
    }
}

